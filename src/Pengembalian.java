    private void boxNamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boxNamaActionPerformed
        // TODO add your handling code here:
        String st = (String) boxNama.getSelectedItem();
        ResultSet rst = con.querySelectAll("tb_transaksi", "nama_peminjam='" + st + "'");
        try {
            while (rst.next()) {
                this.lbl_barang.setText(rst.getString("kode_barang"));
                this.lbl_harga.setText(rst.getString("harga"));
                this.lbl_tgl_peminjaman.setText(rst.getString("tgl_pinjam"));
                this.lbl_tgl_kembali.setText(rst.getString("tgl_kembali"));
                this.lbl_lama.setText(rst.getString("lama"));

            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(Pengembalian.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_boxNamaActionPerformed

