    private void hapus_transaksi() {
        if (JOptionPane.showConfirmDialog(this, "Yakin Mengembalikan Barang ?", "Pengembalian", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            String tglKembaliStr = lbl_tgl_kembali.getText();
            
            try {
                LocalDate tanggalPengembalian = LocalDate.parse(tglKembaliStr, DateTimeFormatter.ofPattern("yyyy-MM-dd"));
                JDateChooser dateChooser = new JDateChooser();
                dateChooser.setDateFormatString("yyyy-MM-dd");

                int dialogResult = JOptionPane.showConfirmDialog(null, dateChooser, "Pilih Tanggal Hari Ini", JOptionPane.OK_CANCEL_OPTION);
                
                if (dialogResult == JOptionPane.OK_OPTION) {
                    Date selectedDate = dateChooser.getDate();
                    LocalDate tanggalHariIni = selectedDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

                    if (tanggalHariIni.isAfter(tanggalPengembalian)) {
                        long denda = tanggalPengembalian.until(tanggalHariIni).getDays() * 25000;

                        String st = (String) boxNama.getSelectedItem();
                        ResultSet rst = con.querySelectAll("tb_transaksi", "nama_peminjam='" + st + "'");   
                        if (rst.next()) {
                            int totalHargaSebelumnya = rst.getInt("total");
                            int totalHargaBaru = totalHargaSebelumnya + (int) denda;

                            JOptionPane.showMessageDialog(this, "Anda terlambat mengembalikan barang.\nDenda yang harus dibayar: Rp " + denda + "\n\nTotal harga yang harus dibayar: Rp " + totalHargaBaru, "Denda", JOptionPane.WARNING_MESSAGE);
                        }
                    } 
                    else {
                        JOptionPane.showMessageDialog(this, "Terima kasih telah mengembalikan barang tepat waktu.", "Laudza Camp", JOptionPane.INFORMATION_MESSAGE);
                    }
                    
                    con.queryDelete("tb_transaksi", "nama_peminjam='" + boxNama.getSelectedItem().toString() + "'");
                }
            } 
            catch (Exception ex) {
                // Tangani exception jika parsing tanggal gagal atau format tanggal salah
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan dalam pengolahan tanggal!", "Error", JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(Pengembalian.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
